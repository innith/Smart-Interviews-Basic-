import java.io.*;
import java.util.*;

class Solution {

    public static void main(String[] args) {
       Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        String os = "";
        for(int i =0;i<n;i++){
            os += "o";
        }
        System.out.println("G"+os+"d");
    }
}


